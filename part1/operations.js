// JavaScript Operators Practice

// Arithmetic Operators
let a = 10;
let b = 3;
console.log('Arithmetic Operators:');
console.log('a + b =', a + b);
console.log('a - b =', a - b);
console.log('a * b =', a * b);
console.log('a / b =', a / b);
console.log('a % b =', a % b);
console.log('2 ** 3 =', 2 ** 3);

// Assignment Operators
let x = 10;
x += 5;
console.log('\nAssignment Operators:');
console.log('x after x += 5 =>', x);

// Comparison Operators
console.log('\nComparison Operators:');
console.log('5 == "5" =>', 5 == "5");
console.log('5 === "5" =>', 5 === "5");
console.log('10 > 5 =>', 10 > 5);
console.log('10 < 5 =>', 10 < 5);
console.log('10 != 5 =>', 10 != 5);

// Logical Operators
let age = 20;
console.log('\nLogical Operators:');
console.log('age > 18 && age < 30 =>', age > 18 && age < 30);
console.log('age < 18 || age > 30 =>', age < 18 || age > 30);
console.log('!true =>', !true);

// Increment/Decrement
let y = 5;
y++;
console.log('\nIncrement Operator:');
console.log('y++ =>', y);

// String Operator
console.log('\nString Operator:');
console.log('"Hello" + " Sanjay" =>', "Hello" + " Sanjay");